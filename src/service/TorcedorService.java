package service;

public class TorcedorService {

	public int Id;
	public String Nome;
	public String IngressoId;

	public int getId() {
		return Id;
	}

	public void setId(int id) {
		Id = id;
	}

	public String getNome() {
		return Nome;
	}

	public void setNome(String nome) {
		Nome = nome;
	}

	public String getIngresso() {
		return IngressoId;
	}

	public void setIngresso(String ingresso) {
		IngressoId = ingresso;
	}
}
